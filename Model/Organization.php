        public function getOrgId($user_id = NULL){
        $this->Behaviors->load('Containable');
            $orgUserDetail = $this->find('first', array(
                'fields' => array('Organization.id'),
                'contain'=>false,
                'conditions'=>array(
                    'Organization.user_id' => $user_id
                )
            ));
            $orgId = $orgUserDetail['Organization']['id'];
            
            return $orgId;
        }

